void trim(std::string &s){ //Delete leading and trailing spaces of a string
	std::size_t len = s.size();
	if(len == 0){
		return;
	}
	std::size_t i = 0;
	while(i < len){
		if(s[i] != ' '){
			break;
		}
	}
	if(i == len){
		s = "";
		return;
	}
	s.erase(0, i); //Delete leading spaces
	len = s.size();

	std::size_t j = len - 1;
	while(j > i){
		if(s[j] != ' '){
			break;
		}
	}
	if(j != len - 1){
		s.erase(j + 1, len - j - 1); //Delete trailing spaces
	}
}

class Solution {
public:
    void sortColors(int A[], int n) {
		int j = 0; 
		int k = n;
		for(int i = 0; i < k; ++i){
			if(A[i] == 0){
				std::swap(A[j++], A[i]);
			}else if(A[i] == 2){
				std::swap(A[--k], A[i--]);//A[i] will be a new element, need to judge again
			}
		}
    }
};

